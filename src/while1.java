public class while1 {
    public static void main(String[] args) {
        /*格式：
        初始化语句;
        while（条件判断语句）{
            循环体语句;
            条件控制语句;
        }
         */
        //需求：利用while循环打印1~100。
        int i = 1;
        while (i <= 100){
            System.out.println(i);
            i++;
        }
    }
}
